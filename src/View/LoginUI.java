    private void bloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bloginActionPerformed
        user = new User();
        
        if(tusername.getText().trim().equals("") || tpassword.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Fill All Values", "Warning!", 2); 
        }else{
            if(user.userValidate(tusername.getText(), tpassword.getText())){ 
                mainui = new MainUI(); 
                mainui.setLogUser(tusername.getText(), mainui); 
                this.dispose();
                
            }else{
                JOptionPane.showMessageDialog(null, "Username or Password are incorrect, Try again!", "Error!", 0);
                tusername.setText("");
                tpassword.setText("");
                tusername.grabFocus();
            }
        }
    }//GEN-LAST:event_bloginActionPerformed

