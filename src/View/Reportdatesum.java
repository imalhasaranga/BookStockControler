    String getReportDate(){
        String repdate = "";
        try {
            if(!dtreport.getDate().equals("")){
                Calendar cal = Calendar.getInstance();
                SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
                repdate = df.format(dtreport.getDate());
        
            }
        } catch (Exception e) {
            System.out.println("Error: " + e);
        }
        
        return repdate;
        
    }
    
    
