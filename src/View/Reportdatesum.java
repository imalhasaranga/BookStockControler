    private void bviewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bviewActionPerformed
        repsummary = new Reportsummary();
        repdate = getReportDate();
        
        if(!tuserid.getText().trim().equals("") && !dtreport.getDate().equals("")){
            userid = Integer.parseInt(tuserid.getText());
            
            totalinvoices = repsummary.getTotalInvoices(repdate, userid);
            totalsoldqty = repsummary.getTotalSoldQty(repdate, userid);
            totalreturnqty = repsummary.getTotalReturnQty(repdate, userid);
            cashinvoices = repsummary.getTotalInvoices(repdate, "cash", userid);
            chequeinvoices = repsummary.getTotalInvoices(repdate, "cheque", userid);
            bothinvoices = repsummary.getTotalInvoices(repdate, "both", userid);
            totalamount = repsummary.getTotalAmount(repdate, userid);
            chequeamount = repsummary.getChequeAmount(repdate, userid);
            chequeamount2 = repsummary.getChequeAmount2(repdate, userid);
            totchequeamount = chequeamount + chequeamount2;
            cashamount = totalamount - totchequeamount;
            returnamount = repsummary.getReturnAmount(repdate, userid);
        
            ldate.setText(repdate); 
            ltotalinvoices.setText(""+totalinvoices);
            ltotalsoldqty.setText(""+totalsoldqty);
            ltotalretqty.setText(""+totalreturnqty);
            lcashinvoices.setText(""+cashinvoices);
            lchequeinvoices.setText(""+chequeinvoices);
            lbothpayinvoices.setText(""+bothinvoices);
            ltotalamount.setText(""+totalamount);
            lchequeamount.setText(""+totchequeamount);
            lcashamount.setText(""+cashamount);
            lreturnamount.setText(""+returnamount);
        }else{
            JOptionPane.showMessageDialog(null, "Type User Id, Select the Date!", "Warning!", 2); 
        }
        
        
        
    }

