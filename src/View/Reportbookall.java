    private void bgenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bgenerateActionPerformed
        clearTable();
        DefaultTableModel dt = (DefaultTableModel) tblbooks.getModel();
        double percentage = 0;
        
        try {
            book = new Book(); 
            repsummary = new Repbooksummary();
            
            ResultSet rs = book.getBooksDetails();
            
                while(rs.next()){
                    Vector vec = new Vector();
                    int bookid = rs.getInt("bookid");
                    String booktitle = rs.getString("title");
                    int soldqty = repsummary.getTotalSoldQty(getFromDate(), getToDate(), bookid);
                    double soldamount = repsummary.getTotalSoldAmount(getFromDate(), getToDate(), bookid);
                    int returnqty = repsummary.getTotalReturnQty(getFromDate(), getToDate(), bookid);
                    double returnamount = repsummary.getTotalReturnAmount(getFromDate(), getToDate(), bookid);
                    
                    if(soldqty != 0){
                        percentage = ((double)returnqty / (double)soldqty) * 100;
                    }else{
                        percentage = 0.0;
                    }
                
                    DecimalFormat decimal = new DecimalFormat("#.##");
                    
                    vec.add(bookid);
                    vec.add(booktitle);
                    vec.add(soldqty);
                    vec.add(soldamount);
                    vec.add(returnqty);
                    vec.add(returnamount);
                    vec.add(Double.valueOf(decimal.format(percentage))); 
                    dt.addRow(vec); 
                    
                }
                
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_bgenerateActionPerformed

