    @Override
    public int print(Graphics g, PageFormat pf, int pageIndex) throws PrinterException {
        if(pageIndex > 0){
            return NO_SUCH_PAGE;
        }
        
        int rows = tblbooks.getRowCount();
        int h = 120;
        
        Graphics2D gr2d = (Graphics2D)g;
        gr2d.translate(pf.getImageableX(), pf.getImageableY());
        
        Font fonth = new Font("arial", Font.BOLD, 12);
        Font fontn = new Font("arial", Font.PLAIN, 10);
        Font fontd = new Font("arial", Font.PLAIN, 8);
        
        g.setFont(fonth); 
        g.drawString("Sathara Prakashakayo", 100, 30); 
        g.setFont(fontn); 
        g.drawString("Books Sales Report", 110, 50);
        
        g.drawLine(20, 60, 300, 60);
        
        g.drawString("From: ", 50, 70);
        g.drawString("" + getFromDate(), 180, 70);
        g.drawString("To: ", 50, 90);
        g.drawString("" + getToDate(), 180, 90);
        g.setFont(fontd); 
        
        g.drawLine(20, 100, 300, 100);
        g.drawString("B.ID", 20, 110);
        g.drawString("Book Title", 50, 110);
        g.drawString("Sold", 150, 110);
        g.drawString("Qty", 150, 120);
        g.drawString("Sold", 180, 110);
        g.drawString("Amount", 180, 120);
        g.drawString("Return", 210, 110);
        g.drawString("Qty", 210, 120);
        g.drawString("Return", 240, 110);
        g.drawString("Amount", 240, 120);
        g.drawString("Percentage", 270, 110);
        g.drawLine(20, 120, 300, 120);
        
        for(int x = 0; x < rows; x++){
            h += 15;
            String bid = tblbooks.getValueAt(x, 0).toString();
            String btitle = tblbooks.getValueAt(x, 1).toString();
            String sqty = tblbooks.getValueAt(x, 2).toString();
            String samount = tblbooks.getValueAt(x, 3).toString();
            String rqty = tblbooks.getValueAt(x, 4).toString();
            String ramount = tblbooks.getValueAt(x, 5).toString();
            String per = tblbooks.getValueAt(x, 6).toString();
            
            g.drawString(bid, 20, h);
            g.drawString(btitle, 50, h);
            g.drawString(sqty, 150, h);
            g.drawString(samount, 180, h);
            g.drawString(rqty, 210, h);
            g.drawString(ramount, 240, h);
            g.drawString(per, 270, h);
            
            
        }
        
        
        return PAGE_EXISTS;
        
    }
