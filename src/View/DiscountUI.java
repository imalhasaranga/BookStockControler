    private void baddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_baddActionPerformed
        if(!tdiscname.getText().trim().equals("") && !tdiscname.getText().trim().equals("")){
            discount = new Discount();
            discount.setDiscountname(tdiscname.getText());
            discount.setPercentage(Integer.parseInt(tdisc.getText())); 
                if(discount.addNewDiscount()){
                    JOptionPane.showMessageDialog(null, "Done! Added Successfully!", "Done!", 1); 
                    loadTable(discount.getAllDiscountDetails());
                    tdiscname.setText(""); 
                    tdisc.setText("");
                }else{
                    JOptionPane.showMessageDialog(null, "Error: While Adding, Try Again!", "Error!", 0);
                }
        }else{
            JOptionPane.showMessageDialog(null, "Fill both Values!", "Warning!", 2);
        }
    }//GEN-LAST:event_baddActionPerformed

    private void bupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bupdateActionPerformed
        if(tbldiscount.getSelectedRow() != -1){
            if(!tdiscname.getText().trim().equals("") && !tdisc.getText().trim().equals("")){
                discount = new Discount(); 
                    if(discount.updateDiscount(selectid, Integer.parseInt(tdisc.getText()))){ 
                        JOptionPane.showMessageDialog(null, "Done! Updated the Discount Successfully!", "Done!", 1);
                        loadTable(discount.getAllDiscountDetails());
                        tdiscname.setText(""); 
                        tdisc.setText("");
                    }else{
                        JOptionPane.showMessageDialog(null, "Error! While updating Discounts!", "Error!", 0);
                    }
            }else{
                JOptionPane.showMessageDialog(null, "Aill All Details!", "Warning!", 2);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Select the Discount from Table", "Warning!", 2);
        }
    }//GEN-LAST:event_bupdateActionPerformed

